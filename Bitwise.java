/**
 * Bitwise
 */
public class Bitwise {

    public static void main(String args[]) {
        boolean a= true;
        boolean b= false;
        int c= 11;
        int d= 22;
        System.out.println("!a   "+ !a);
        System.out.println("!b   "+ !b);
        System.out.println("~c   "+ ~c);
        System.out.println("~d   "+ ~d);
        System.out.println("c<<2   "+ (d<<2) );
        System.out.println("d>>2   "+ (c>>2) );
        
        
    }
}