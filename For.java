/**
 * For
 */
public class For {

    public static void main(String[] args) {
        for (int i = 0; i <= 5; i++) {
            System.out.println("i= "+ i);
            
        }
        int[] arr={10,20,30};
        for (int n : arr) {
            System.out.println(n);
            
        }
    }
}