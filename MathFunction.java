/**
 * MathFunction
 */
public class MathFunction {

    public static void main(String args[]){
        System.out.println("Ceil :"+ Math.ceil(4.67));
        System.out.println("FLoor :"+ Math.floor(4.67));
        System.out.println("Absolute :"+ Math.abs(4.67));
        System.out.println("MAx :"+ Math.max(4.67,11.99));
        System.out.println("Min :"+ Math.min(4.67,332));
        System.out.println("Power :"+ Math.pow(3,3));
        System.out.println("Square root :"+ Math.sqrt(4.67));
        
        
        
    }
}