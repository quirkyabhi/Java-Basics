/**
 * Logical
 */
public class Logical {

    public static void main(String[] args) {
        boolean a= true;
        boolean b= false;
        boolean c= true;
        System.out.println("Logical &&: " + (a&&b));
        System.out.println("Logical ||: " + (a||b));
        System.out.println("Logical &&: " + (a&&c));
        System.out.println("Logical ||: " + (a||c));
    }
}